#include "sh21.h"

int		is_escape(char *str, int i)
{
	if (!str || i == 0)
		return (0);
	if (ft_strlen(str) == 0)
		return (0);
	if (str[i - 1] == '\\')
		return (1);
	else
		return (0);
}
