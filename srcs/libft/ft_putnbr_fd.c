#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long j;

	j = n;
	if (j < 0)
	{
		j = j * -1;
		ft_putchar_fd('-', fd);
	}
	if (j >= 10)
		ft_putnbr_fd(j / 10, fd);
	ft_putchar_fd((j % 10) + '0', fd);
}
