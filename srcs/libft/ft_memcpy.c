#include <string.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char	*str;
	size_t	i;

	i = 0;
	str = (char*)src;
	while (i < n)
	{
		((char*)dest)[i] = str[i];
		i++;
	}
	return (dest);
}
