#include "libft.h"

t_list		*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*buf;

	if (lst == NULL)
		return (NULL);
	buf = (t_list *)ft_malloc(sizeof(t_list));
	buf = f(lst);
	if (lst->next != NULL)
		buf->next = ft_lstmap(lst->next, *f);
	return (buf);
}
